package com.neotech.lesson08;

public class IncrementandDecrement {

	public static void main(String[] args) {
		
		int x = 10;
		
		// Increment any form works
		x = x + 1;
		
		x += 1;
		// easiest increases by 1 and is the shortest
		x++;
		
		// Decrement
		
		int y = 15;
		
		y = y - 1;
		// shorthand
		y -= 1;
		
		y--;
		
		
		
		
		
		
		
		
	}
}
