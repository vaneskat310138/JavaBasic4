package com.neotech.lesson04;

public class PrimitiveCasting {

	public static void main(String[] args) {

		double d1 = 3.7;
		System.out.println(d1);

		double d2 = 6;
		System.out.println(d2);

		int num1 = 5;
		double d3 = num1;
		System.out.println(d3);

		int num2 = (int) 6.3;
		System.out.println(num2);
		


	}

}
