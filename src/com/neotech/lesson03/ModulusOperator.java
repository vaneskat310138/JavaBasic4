package com.neotech.lesson03;

public class ModulusOperator {

	public static void main(String[] args) {
		
		int a = 25;
		int b = 4;
		
		int div = a/b;
		
		int remainder = a%b;
		
		System.out.println(div);
		System.out.println(remainder);
		
		double d1 = 3.656;
		double d2 = 4.323;
		double d3 = d1/d2;
		
		System.out.println(d3);
		
		int c = 13;
		int d = 5;
		
		int rem = c%d;
		
		System.out.println(rem);

		
		
		
		
	}
}
